    "name" => "Laptop",
    "brand" => "Dell",
    "price" => 1200
];

// Accessing elements in the associative array
echo "Product Name: " . $product["name"] . "<br>";
echo "Brand: " . $product["brand"] . "<br>";
echo "Price: $" . $product["price"] . "<br>";
?>
